    public String autoNumber() {
        ModelPersediaan mk = new ModelPersediaan();
        String number = mk.getId();
        if (number.equals("")) {
            number = "K001";
        } else {
            number = number.substring(2);
            int angka = Integer.parseInt(number);
            angka++;
            if (angka < 10) {
                number = "00" + angka;
            } else if (angka >= 10 && angka < 100) {
                number = "0" + angka;
            } else if (angka >= 100 && angka < 1000) {
                number = "" + angka;
            } else {
                number = String.valueOf(angka);
            }
            if (number.length() > 5) {
                number = number.substring(number.length() - 5, number.length());
            }
            number = "K" + number;
        }
        return number;
    }

    public void loadId() {
        txtKodeBarang.setText(autoNumber());
    }

    public void loadData() {
        ModelPersediaan m = new ModelPersediaan();
        ResultSet r = m.read();
        try {
            model.getDataVector().removeAllElements();
            while (r.next()) {
                Object obj[] = new Object[4];
                obj[0] = r.getString("kd_persediaan");
                obj[1] = r.getString("nama_kategori");
                obj[2] = r.getString("satuan");
                obj[3] = r.getString("stok");                
                model.addRow(obj);
            }
        } catch (SQLException ex) {
            Logger.getLogger(FrmPersediaan.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void loadJenis() {
        ModelJenis m = new ModelJenis();
        ResultSet r = m.read();
        try {
            while (r.next()) {
                cbbJenis.addItem(r.getString("nama_kategori"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(FrmPersediaan.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
