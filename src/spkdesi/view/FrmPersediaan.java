    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        ModelPersediaan mh = new ModelPersediaan();
        ResultSet r = mh.selectJenis(cbbJenis.getSelectedItem().toString());
        try {
            r.next();
            String kd = r.getString("kd_kategori");
            String a = txtUkuran.getText();
            int stok = Integer.valueOf(txtStok.getText());
            if(stok > 0 ){
            if (!a.equals("")) {
                String data[] = {txtKodeBarang.getText(), kd,
                    txtUkuran.getText(), txtStok.getText()
                };
                if (mh.insert(data) > 0) {
                    JOptionPane.showMessageDialog(null, "Data Masuk");
                    loadId();
                } else {
                    JOptionPane.showMessageDialog(null, "Gagal");
                }
                loadData();
                btnBaru.setText("Baru");
                btnSimpan.setEnabled(false);
                btnUpdate.setEnabled(true);
                btnHapus.setEnabled(true);
                txtUkuran.setEditable(false);
                txtStok.setEditable(false);                
                cbbJenis.setSelectedItem(" ");
                txtKodeBarang.setText("");
                txtUkuran.setText("");
                txtStok.setText("");
               
            } else {
                JOptionPane.showMessageDialog(null, "Inputkan semua data barang", "Notifikasi", JOptionPane.INFORMATION_MESSAGE);
            }
            }else{
                JOptionPane.showMessageDialog(null, "nilai tidak boleh minus");
            }
        } catch (SQLException ex) {
            Logger.getLogger(FrmPersediaan.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        if (btnUpdate.getText().equals("Edit")) {
            btnUpdate.setText("Update");
            btnSimpan.setEnabled(false);
            btnUpdate.setEnabled(true);
            btnHapus.setEnabled(false);
            btnBaru.setText("Batal");
            txtUkuran.setEditable(true);
            txtStok.setEditable(true);           
            cbbJenis.setEnabled(true);
        } else {
            //Fungsi update            
            ModelPersediaan mu = new ModelPersediaan();
            ResultSet r = mu.selectJenis(cbbJenis.getSelectedItem().toString());

            String a = txtUkuran.getText();
            if (txtUkuran.toString().isEmpty()) {
                JOptionPane.showMessageDialog(null, "inputkan semua data");
            } else {
                try {
                    r.next();
                    String kd = r.getString("kd_kategori");
                    String data[] = {txtKodeBarang.getText(), kd,
                       txtUkuran.getText(), txtStok.getText()
                    };
                    if (mu.update(data) > 0) {
                        loadData();
                        JOptionPane.showMessageDialog(null, "Data terupdate");
                        cbbJenis.setSelectedItem(" ");
                        txtUkuran.setText("");
                        txtStok.setText("");                        
                    } else {
                        JOptionPane.showMessageDialog(null, "Gagal");
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(FrmPersediaan.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            btnBaru.setText("Baru");
            btnUpdate.setText("Edit");
            btnSimpan.setEnabled(false);
            btnUpdate.setEnabled(false);
            btnHapus.setEnabled(false);
            txtUkuran.setEditable(false);
            txtStok.setEditable(false);            
            cbbJenis.setEnabled(false);
            cbbJenis.setSelectedItem(" ");
            txtKodeBarang.setText("");
            txtUkuran.setText("");
            txtStok.setText("");
            
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        int i = tblPersediaan.getSelectedRow();
        String kd_persediaan = (String) tblPersediaan.getValueAt(i, 0);
        ModelPersediaan mk = new ModelPersediaan();
        String data[] = {kd_persediaan};
        if (JOptionPane.showConfirmDialog(this, "Yakin menghapus data?", "Peringatan", JOptionPane.OK_CANCEL_OPTION) == JOptionPane.OK_OPTION) {
            mk.delete(data);
            JOptionPane.showMessageDialog(null, "Data terhapus");
            //
            loadData();
            btnBaru.setText("Baru");
            btnSimpan.setEnabled(false);
            btnUpdate.setEnabled(false);
            btnHapus.setEnabled(false);
            txtUkuran.setEditable(false);
            txtStok.setEditable(false);           
            cbbJenis.setEnabled(false);
            cbbJenis.setSelectedItem(" ");
            txtKodeBarang.setText("");
            txtUkuran.setText("");
            txtStok.setText("");
            
        } else {
            JOptionPane.showMessageDialog(null, "Batal Menghapus");
        }
    }//GEN-LAST:event_btnHapusActionPerformed

