    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        int i = tblHasilPeramalan.getSelectedRow();
        String kd_peramalan = (String) tblHasilPeramalan.getValueAt(i, 0);
        ModelPeramalan mk = new ModelPeramalan();
        String data[] = {kd_peramalan};
        if (JOptionPane.showConfirmDialog(this, "Yakin menghapus data?", "Peringatan", JOptionPane.OK_CANCEL_OPTION) == JOptionPane.OK_OPTION) {
            mk.delete(data);
            JOptionPane.showMessageDialog(null, "Data terhapus");
            //
            loadData();            
            btnHapus.setEnabled(false);           
            btnBatal.setEnabled(false);           
            txtKd.setText("");
        } else {
            JOptionPane.showMessageDialog(null, "Batal Menghapus");
        }
    }//GEN-LAST:event_btnHapusActionPerformed

