    public void loadData(){
        ModelPeramalan md = new ModelPeramalan();
        ResultSet r = md.read();
        model.getDataVector().removeAllElements();
        try {
            while(r.next()){
                Object obj[] = new Object[7];
                obj[0] = r.getString("kd_peramalan");
                obj[1] = r.getString("tgl_peramalan");
                obj[2] = r.getString("nama_kategori");
                obj[3] = r.getString("satuan");
                obj[4] = r.getString("alpha");
                obj[5] = r.getString("mse");
                obj[6] = r.getString("hasil");
                model.addRow(obj);
            }
        } catch (SQLException ex) {
            Logger.getLogger(FrmPeramalan.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
