    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        ModelBeli mh = new ModelBeli();
        ResultSet r = mh.selectJenis(cbbPemasok.getSelectedItem().toString());
        SimpleDateFormat df = new SimpleDateFormat("yyyy-MM-dd");
        String tgl = df.format(Calendar.getInstance().getTime());
        try {
            r.next();
            String kd = r.getString("kd_pemasok");
            String a = txtUkuran.getText();
            if (!a.equals("")) {
                String data[] = {txtKodeBeli.getText(), kd, txtKodeBarang.getText(),
                    tgl, txtJumlah.getText(), txtHarga.getText()
                };
                if (mh.insert(data) > 0) {
                    JOptionPane.showMessageDialog(null, "Data Masuk");
                } else {
                    JOptionPane.showMessageDialog(null, "Gagal");
                }
                loadTblBeli();
                btnBaru.setText("Baru");
                btnSimpan.setEnabled(false);
                btnUpdate.setEnabled(true);
                btnHapus.setEnabled(true);
                txtKodeBeli.setEditable(false);
                txtUkuran.setEditable(false);
                txtTgl.setEnabled(false);
                txtJumlah.setEditable(false);
                btnPilih.setEnabled(false);
                cbbPemasok.setSelectedItem(" ");
                txtKodeBeli.setText("");
                txtKodeBarang.setText("");
                txtJenis.setText("");
                txtUkuran.setText("");
                txtJumlah.setText("");
                txtHarga.setText("");
                txtTgl.setText("");
            } else {
                JOptionPane.showMessageDialog(null, "Inputkan semua data barang", "Notifikasi", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (SQLException ex) {
            Logger.getLogger(FrmBeli.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

