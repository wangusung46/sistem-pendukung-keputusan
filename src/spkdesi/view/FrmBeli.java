    public void loadTblBeli() {
        ModelBeli m = new ModelBeli();
        ResultSet r = m.read_beli();
        try {
            model.getDataVector().removeAllElements();
            while (r.next()) {
                Object obj[] = new Object[7];
                obj[0] = r.getString("kd_beli");
                obj[1] = r.getString("tgl_beli");
                obj[2] = r.getString("nama_kategori");
                obj[3] = r.getString("satuan");
                obj[4] = r.getString("nama_pemasok");
                obj[5] = r.getString("jml_barang");
                obj[6] = r.getString("harga");
                model.addRow(obj);
            }
        } catch (SQLException ex) {
            Logger.getLogger(FrmPakai.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

